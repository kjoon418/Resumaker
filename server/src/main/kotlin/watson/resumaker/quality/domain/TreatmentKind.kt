package watson.resumaker.quality.domain

/**
 * 개선 소견의 **처치 종류**(품질 개선 기획 §2·§3.1 — 이 기능의 핵심 골격).
 *
 * 같은 안티패턴이라도 **경험 기록에 근거가 존재하는가**라는 결정적 조건으로 갈린다(§130 분기 규칙):
 * - [AUTO_REWRITE]: 사실(수치·고유명사·성과)을 불변으로 둔 채 표현·구조·강조·간결만 다듬는다. 신뢰성 검증 +
 *   원본 사실 토큰 보존 검증을 통과한 것만 채택 후보가 된다. 처치는 비동기 품질 개선 작업이 LLM으로 수행한다.
 * - [SUGGESTION]: 자동으로 값을 만들 수 없어 **사용자 액션이 필요한 보강 안내**. 서비스는 텍스트를 바꾸지 않고
 *   "이 경험에 이런 사실을 더하면 강해져요"라고 알린다(§469 빈자리는 채우지 않고 알린다). 채택 대상이 아니다.
 * - [OUT_OF_SCOPE]: 텍스트 산출물이 아니라 시각·서식·파일·외부 자산 영역이라 MVP 자동 개선이 손대지 않는다
 *   (R1·AP14·AP15 — QC12). 진단·고지로만 다룬다.
 *
 * enum 상수명은 클라이언트와 1:1로 직렬화되므로 그대로 유지한다(프론트가 자동 적용/개선 제안 UI를 분기 — §243).
 */
enum class TreatmentKind {
    AUTO_REWRITE,
    SUGGESTION,
    OUT_OF_SCOPE,
}
